#pragma once
//----------------------------------------------------------------------------------------------------------------------
#include <boost/locale.hpp>
//----------------------------------------------------------------------------------------------------------------------
#include <string>
#include <string_view>
//---------------------------------------------------------------------------------------------------------------------

inline std::wstring toWideString(std::string_view narrow)
{
    return boost::locale::conv::utf_to_utf<wchar_t>(narrow.data());
}

